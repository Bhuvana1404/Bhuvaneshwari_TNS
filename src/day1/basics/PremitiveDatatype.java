package day1.basics;

public class PremitiveDatatype {

	public static void main(String[] args) {
		//BYTE
		byte  min=25;
		byte max=100;
		System.out.println("Maximum Byte number is:"+max);
		System.out.println("Minimum Byte number is:" +min );
		
 
         //SHORT
         short smin=22552;
         short smax=23669;
         System.out.println("\nMaximum Shortnumber is:"+smax);
 		System.out.println("Minimum Shortnumber is:" +smin);
	
 		  //LONG
        long lmin=225525885567664l;
        long lmax=23669885567946l;
        System.out.println("\nMaximum Longnumber is:"+lmax);
		System.out.println("Minimum Longtnumber is:" +lmin);
		
		  //INTEGER
        int imin=225525856;
        int imax=236698854;
        System.out.println("\nMaximum Integer is:"+imax);
		System.out.println("Minimum Integer is:" +imin);
		
		  //BOOLEAN
        boolean x=true;
        System.out.println("\nBoolean x is:"+x);
        
        //FLOAT
        float fmin=22.5525885567664f;
        float fmax=2366.9885567946f;
        System.out.println("\nMaximum floatnumber is:"+fmax);
		System.out.println("Minimum floatnumber is:" +fmin);
        
        }
}